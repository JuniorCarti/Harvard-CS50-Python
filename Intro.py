# Introduction to Python programming language

# What is Python?
# Python is a popular programming language.
# It was created by Guido van Rossum and released in 1991.

# Python is used for various purposes, including:
# - Web development (server-side)
# - Software development
# - Mathematics
# - System scripting

# What can Python do?
# Python can be used on a server to create web applications.
# Python can be used alongside software to create workflows.
# Python can connect to database systems, allowing it to read and modify files.
# Python can handle big data and perform complex mathematical operations.
# Python is suitable for rapid prototyping as well as production-ready software development.

# Why Python?
# Python works on multiple platforms, such as Windows, Mac, Linux, and Raspberry Pi.
# Python has a simple syntax that is similar to the English language.
# Python's syntax allows developers to write programs with fewer lines of code compared to other programming languages.
# Python runs on an interpreter system, enabling code execution as soon as it is written, which makes prototyping very quick.
# Python supports multiple programming paradigms, including procedural, object-oriented, and functional programming.

# Good to know:
# - The most recent major version of Python is Python 3, which will be used in this tutorial.
# - Python 2 is still popular but only receives security updates and no new features.
# - Python code can be written in a text editor or an Integrated Development Environment (IDE).
# - Examples of IDEs include Thonny, PyCharm, NetBeans, and Eclipse, which are useful for managing larger Python projects.

# Python Syntax compared to other programming languages:
# - Python was designed for readability and has similarities to the English language, with influences from mathematics.
# - Python uses new lines to complete a command, unlike other programming languages that often use semicolons or parentheses.
# - Python relies on indentation (whitespace) to define scope, such as the scope of loops, functions, and classes.
# - Other programming languages often use curly brackets to define scope.

print("Hello, World!")  # This is a comment. It will not be executed.
# The print() function is used to output data to the standard output device (screen).   
# The parentheses are used to enclose the data to be printed.
# The string "Hello, World!" is enclosed in double quotes, indicating that it is a string literal.
# Strings can also be enclosed in single quotes, e.g., 'Hello, World!'
# The print() function can also be used to print multiple strings or variables by separating them with commas.
# For example: print("Hello", "World!") will output: Hello World!
print("Hello", "World!")  
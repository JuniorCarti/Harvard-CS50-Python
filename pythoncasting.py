#Casting
#Casting is the process of converting a value from one data type to another. In Python, you can use the following functions to cast values:
# - int(): Converts a value to an integer.
# - float(): Converts a value to a float.
# - str(): Converts a value to a string.
# - bool(): Converts a value to a boolean.
# - list(): Converts a value to a list.
# - tuple(): Converts a value to a tuple.
# - set(): Converts a value to a set.
# - dict(): Converts a value to a dictionary.
# - complex(): Converts a value to a complex number.
# - bytes(): Converts a value to bytes.
# - bytearray(): Converts a value to a bytearray.
# - memoryview(): Converts a value to a memoryview.
# - frozenset(): Converts a value to a frozenset.
# - range(): Converts a value to a range object.
# - slice(): Converts a value to a slice object.
# - type(): Returns the type of a value.
# - isinstance(): Checks if a value is of a specified type.
# - issubclass(): Checks if a class is a subclass of another class.
# - id(): Returns the identity of a value.
# - dir(): Returns a list of the attributes and methods of a value.
# - help(): Displays the documentation of a value.
# - eval(): Evaluates a string as a Python expression.
# - exec(): Executes a string as a Python statement.


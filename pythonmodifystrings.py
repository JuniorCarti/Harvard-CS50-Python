#Python has a set of built-in methods that you can use on strings.
#Upper Case
#The upper() method returns the string in upper case
#x = "Hello World"
#y = "Hello World"
x = "Hello World"
y = "Hello World"
print(x.upper())
print(y.upper())

#Lower Case
#The lower() method returns the string in lower case
#x = "Hello World"
#y = "Hello World"
x = "Hello World"
y = "Hello World"
print(x.lower())
print(y.lower())

#Remove Whitespace
#The strip() method removes any whitespace from the beginning or the end of a string        
#x = " Hello World "
#y = " Hello World "
x = " Hello World "
y = " Hello World "
print(x.strip())
print(y.strip())
